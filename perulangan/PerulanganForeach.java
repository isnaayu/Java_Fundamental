public class PerulanganForeach {
    public static void main(String[] args){
        // membuat array;
        int angka[] = {3, 1, 42, 24, 12};

        // Menggunakan perulangan for each untuk menampilkan angka
        for(int x : angka){
            System.out.print(x + " ");
        }
        System.out.println();
        
    }
    
}
